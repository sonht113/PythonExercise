# PROBLEM:
#     1. List two examples of input devices and two examples of output devices.
#     2. What does the central processing unit (CPU) do?
#     3. How is information represented in hardware memory?
#     4. What is the difference between a terminal-based interface and a graphical user interface?
#     5. What role do translators play in the programming process?
#
#
# SOLUTION:
#     1)
#         Input devices:   Mouse,Keyboard,Scanner,Microphone,Webcam,....
#         Output devices:  Monitor,Printer,Speaker
#     2)
#         A central processing unit (CPU), also called a central processor, main processor or just processor, is the electronic circuitry that executes instructions comprising a computer program.
#         The CPU performs basic arithmetic, logic, controlling, and input/output (I/O) operations specified by the instructions in the program.
#     3)
#         Information is stored in the hardware memory and is represented in binary digits. Input and output devices transmit the information from the memory to the external world.
#     4)
#         A terminal-based interface accepts inputs from a keyboard and displays text output on a monitor screen.
#         A graphical user interface (GUI) organizes the monitor screen around the metaphor of a desktop, with windows containing icons for folders, files, and applications.
#     5)
#         A translator or programming language processor is a generic term that can refer to anything that converts code from one computer language into another
#         A program written in high-level language is called source program.
